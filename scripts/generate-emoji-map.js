/**
 * Generates src/utils/emojiData.ts from emoji-datasource.
 * Run: node scripts/generate-emoji-map.js
 *
 * This uses the same emoji dataset that Slack uses internally,
 * so all standard Slack shortcodes are covered.
 */

const data = require("emoji-datasource");

// Build shortcode -> unicode emoji map
const map = {};

for (const entry of data) {
	// Convert unified codepoint string (e.g. "1F64C") to emoji character
	const emoji = entry.unified
		.split("-")
		.map((cp) => String.fromCodePoint(parseInt(cp, 16)))
		.join("");

	// Add all short_names for this emoji
	for (const name of entry.short_names) {
		if (!map[name]) {
			map[name] = emoji;
		}
	}
}

// Sort keys alphabetically for consistent output
const sorted = Object.keys(map).sort();

// Generate TypeScript file
let output = `// Auto-generated by scripts/generate-emoji-map.js — do not edit manually\n`;
output += `// Source: emoji-datasource (${data.length} emoji, ${sorted.length} shortcodes)\n`;
output += `// Run: node scripts/generate-emoji-map.js\n\n`;
output += `export const EMOJI_DATA: Record<string, string> = {\n`;

for (const key of sorted) {
	const emoji = map[key];
	// Encode as unicode escapes for safe TS
	const escaped = [...emoji]
		.map((ch) => {
			const cp = ch.codePointAt(0);
			if (cp > 0xffff) {
				return `\\u{${cp.toString(16).toUpperCase()}}`;
			} else if (cp > 0x7e || cp < 0x20) {
				return `\\u${cp.toString(16).toUpperCase().padStart(4, "0")}`;
			}
			return ch;
		})
		.join("");
	output += `\t${JSON.stringify(key)}: "${escaped}",\n`;
}

output += `};\n`;

const fs = require("fs");
const path = require("path");
const outPath = path.join(__dirname, "..", "src", "utils", "emojiData.ts");
fs.writeFileSync(outPath, output);
console.log(`Generated ${outPath} with ${sorted.length} shortcodes`);
